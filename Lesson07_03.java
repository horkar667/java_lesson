class Lesson07_03 {
  // 文字系のデータ型
  public static void main(String[] args) {
    char var01 = 'c'; // 文字一つの時に使う シングルクォーテーション
    String var02 = "hello,World"; // 文字二つ以上の時に使う ダブルクォーテーション
//  ↑ sが大文字

    System.out.println(var01);
    System.out.println(var02);
  }
}
// Stringは正確には、データ型ではなくStringというクラス