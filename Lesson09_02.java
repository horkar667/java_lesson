class Lesson09_02 {
  public static void main(String[] args) {
    // 関係演算子
    int x = 10;
    int y = 2;
    int z = 10;

    System.out.println(x > y);
    System.out.println(x < y);
    System.out.println(x >= y);
    System.out.println(x <= y);
    System.out.println(x <= z);
    System.out.println(y == z);
    System.out.println(y != z);

  }
}
