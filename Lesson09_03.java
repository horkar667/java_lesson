class Lesson09_03 {
  public static void main(String[] args) {
    // 論理演算子
    int x = 8;
    int y = 3;

    System.out.println(x >= 5 && x <= 10); //true
    System.out.println(y >= 5 && y <= 10); //false
    System.out.println(y >= 5 && y <= 10); //false
    System.out.println(x == 3 || y == 3); //true
    System.out.println(x == 1 || y == 1); //false
  }
}
